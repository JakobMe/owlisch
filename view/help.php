    <!--Wie funktioniert das Quiz?-->
    <h4 class="subtitle big">
        <span class="text">
            Wie funktioniert das Quiz?
        </span>
    </h4>
    <p class="text">
        Im <b>Quiz</b> werden sowohl neue, als auch schon erlernte Wörter
        abgefragt. Dabei unterscheidet man zwischen drei <b>Schwierigkeitsstufen</b>
        (<i>leicht, mittel, schwer</i>), die im Quiz an den Sternen
        <i class="fa fa-star"></i> zu erkennen sind, und verschiedenen
        <b>Quizformen</b>, die sich nach dem Schwierigkeitsgrad richten. Als
        zusätzliche Funktion kann man sich mit <i class="fa fa-volume-up"></i>
        das gesuchte (ostwestfälische) Wort auch vorsprechen lassen.
    </p>
    <p class="text">
        Das Quiz kann jeder Zeit mit <i class="fa fa-close"></i>
        <b>abgebrochen</b> werden. Mit <i class="fa fa-step-forward"></i>
        können einzelne Fragen <b>übersprungen</b> werden. 
    </p>
    
    <!--Was macht das Wörterbuch?-->
    <h4 class="subtitle big">
        <span class="text">
            Was macht das Wörterbuch?
        </span>
    </h4>
    <p class="text">
        Im <b>Wörterbuch</b> werden alle Wörter, die im Quiz vom Benutzer
        erraten wurden, automatisch abgespeichert. Hierbei kann der Nutzer über
        den rechts oben angebrachten Menüpunkt <i class="fa fa-sort"></i> aus
        verschiedenen <b>Sortieroptionen</b> wählen (<i>alphabetisch
        aufwärts/abwärts, nach Stufe aufwärts/abwärts</i>) und mit
        <i class="fa fa-search"></i> nach Wörtern <b>suchen</b>.
    </p>
    <p class="text">
        Die einzelnen Wörterbucheinträge bestehen jeweils aus der
        Übersetzung des Wortes, einer Beschreibung und optional einem Bild.
        Auch hier ist die <b>Audio</b>-Funktion <i class="fa fa-volume-up"></i>
        verfügbar. 
    </p>
    
    <!--Was zeigt mir der Fortschritt?-->
    <h4 class="subtitle big">
        <span class="text">
            Was zeigt mir der Fortschritt?
        </span>
    </h4>
    <p class="text">
        Im <b>Fortschrittsmenü</b> kann der Nutzer einsehen, wie viele richtige
        Antworten er in den letzten <b>zehn</b> Durchgängen im Quiz gegeben hat. 
        Weiterhin wird noch angezeigt, wie viele der <b>vorhandenen Wörter</b>
        bereits gelernt wurden und wie viele Wörter der drei unterschiedlichen
        <b>Schwierigkeitsstufen</b> sich im Wörterbuch befinden.
    </p>

